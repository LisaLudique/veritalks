<h3>About Veritalks</h3>

<p class='veritalks'>
<b>Welcome to Veritalks!</b><br>
Veritalks is a Harvard student-run website whose purpose is to provide genuine, uncensored student answers to anonymously submitted questions on everything Harvard-related. Ultimately, we hope to provide an accurate depiction of the Harvard experience within an environment that is free from judgement. 
<br>
Feel free to ask a question or offer some advice. A moderator will promptly answer or approve your submission! 
<br><br>


The following tags will be used to simplify usage:
<br>
<b>Academics:</b> classes, concentrations, extracurriculars,  research opportunities, grades and advising.
<br>
<b>Social Scene:</b> parties, greek life, night life, social organizations, cultural organizations, final clubs and all-things-Cambridge.
<br>
<b>Student Life:</b> friendships, relationships, recreation, living spaces, dining, race/LGBT/gender concerns and safety.
<br>
<b>Real World:</b> graduate education, internships, careers, interview tips, networking.
<br>
<b>Prospective Students:</b> application tips, application process, unique qualities about Harvard. 
<br>
<b>Financial Aid:</b> general financial aid, work-study aid, funding opportunities, grants and scholarships 
</p>


